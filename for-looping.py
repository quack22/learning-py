"""
Program perulangan menonton DVD dengan menggunakan FOR
"""

jumlahDVD = 100
print('Ayah berkata, "Tonton semua DVD-mu"')

telahDitonton = 0
print(f"jumlah DVD yang telah ditonton {telahDitonton} judul")

print("Dengan for")
for telahDitonton in range(1, jumlahDVD+1):
    print(f"Buku ke {telahDitonton} sudah ditonton")

print(f"Jumlah DVD yang sudah ditonton {telahDitonton} judul")